    public class Pintando extends Thread 
    {
        boolean bandera = false;
        @Override
        public void run() throws NoSuchMethodError {
            while (true) {
                int i = 1;
                int x = 0;
                //mientra el trabajo en paralelo no finalice el jProgressBar continuara su animacion una y otra vez
                while (x <= tiempo) 
                {
                    try {
                        Thread.sleep(100);
                        i = (i > 100) ? 1 : i + 1;
                        barraProgreso.setValue(i);
                        barraProgreso.repaint();

                    } catch (InterruptedException e) {
                    }
                    x++;
                }
                bandera = true;
                if (bandera==true)
                {
                    barraProgreso.setValue(100);
                    JOptionPane.showMessageDialog(null, "La Copia de Seguridad se ha realizado con Ã©xito en "+ URL, null, JOptionPane.INFORMATION_MESSAGE);
                    break;//rompe ciclo   
                }                
            }
            
        }
    }
    
  public void realizaBackup(String URL) {

        Calendar unDate = Calendar.getInstance();
        String fechaActual = String.valueOf(unDate.get(Calendar.DAY_OF_MONTH)) + "_" + String.valueOf(unDate.get(Calendar.MONTH) + 1) + "_" + String.valueOf(unDate.get(Calendar.YEAR));
        String horaActual = "_" + String.valueOf(unDate.get(Calendar.HOUR_OF_DAY)) + "_" + String.valueOf(unDate.get(Calendar.MINUTE));

        final String[] comandos = new String[15];
        
        comandos[0] = "C:/Program Files (x86)/PostgreSQL/9.2/bin/pg_dump.exe";
        comandos[1] = "-i";
        comandos[2] = "-h";
        comandos[3] = "localhost";     //ou  comandos.add("192.168.0.1");   
        comandos[4] = "-p";
        comandos[5] = "5432";
        comandos[6] = "-U";
        comandos[7] = "postgres";
        comandos[8] = "-F";
        comandos[9] = "c";
        comandos[10] = "-b";
        comandos[11] = "-v";
        comandos[12] = "-f";
        comandos[13] = URL+"/copia (" + fechaActual + horaActual + ").backup";
        comandos[14] = "Remiseria";

        ProcessBuilder pb = new ProcessBuilder(comandos);

        pb.environment().put("PGPASSWORD", "lagarto");

       try {
            final Process process = pb.start();
            try (BufferedReader r = new BufferedReader(
                new InputStreamReader(process.getErrorStream()))) {
                String line = r.readLine();
                while (line != null) {
                    //System.err.println(line);
                    line = r.readLine();
                }
            }
            process.waitFor();
            process.destroy();
        } 
       catch (IOException | InterruptedException e)
       {
       
       }
    }  
