    public static void limpiar_tabla(JTable tabla) {
        int filas = tabla.getRowCount();
        for (int i = 0; i < filas; i++) {
            ((DefaultTableModel) tabla.getModel()).removeRow(0);
        }
    }    
    
    
    public  void cargarTablaAuditoria(JTable tabla, List<ImprimirAuditoria> lista) {
        try
        {
        limpiar_tabla(tabla);
        String[] filas = new String[4];
        Collections.sort(lista, new Comparator<ImprimirAuditoria>() {
                @Override
                public int compare(ImprimirAuditoria p1, ImprimirAuditoria p2) {                
                        return new Long(p1.getMomento().getTime()).compareTo(new Long(p2.getMomento().getTime()));
                }
        });        
       // for (Entidad_Revisada lala : lista) {
        for (ImprimirAuditoria lala : lista) {    
           // filas[0] = String.valueOf(lala.getId());//getCustomTimestamp());
            filas[0] = lala.getAccion();
            filas[1] = lala.getEntityClassName();
            filas[2] = lala.getUsuario();
            filas[3] = utilidades.getFecha(lala.getMomento())+"-"+ utilidades.getHora(lala.getMomento());
            ((DefaultTableModel) tabla.getModel()).addRow(filas);
        }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,"Error al cargar la Tabla de Logs");
        }
    }    
    
    public static void cargarTablaAuditoria2(JTable tabla, List<Auditoria_Operacional> lista) {
        limpiar_tabla(tabla);
        String[] filas = new String[5];
        for (Auditoria_Operacional lala : lista) {
            filas[0] = String.valueOf(lala.getId());//getCustomTimestamp());
            filas[1] = lala.getOperacion();
            filas[2] = lala.getDetalle();
            filas[3] = lala.getUsuarioResponsable();
            filas[4] = lala.getFecha().toString();
            ((DefaultTableModel) tabla.getModel()).addRow(filas);
        }

    }      
    
